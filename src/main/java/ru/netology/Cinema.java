package ru.netology;

public class Cinema {
    private int indexId;
    private int id;
    private String style;
    private String name;
    private String picture;

    public Cinema(int indexId, int id, String style, String name, String picture) {
        this.indexId = indexId;
        this.id = id;
        this.style = style;
        this.name = name;
        this.picture = picture;
    }

}